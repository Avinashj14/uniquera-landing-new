import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import postcss from 'postcss';
import prefixSelector from 'postcss-prefix-selector';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const PREFIX = '.uniquera-form-wrap';

/** Order matters: libs first so main_v2 overrides stay last. */
const INPUTS = [
  'uniquera-consultation-form/assets/css/nouislider.min.css',
  'uniquera-consultation-form/assets/css/intlTelInput.css',
  'uniquera-consultation-form/assets/css/main_v2.css',
];

const mergedRaw = INPUTS.map((rel) =>
  fs.readFileSync(path.join(root, rel), 'utf8')
).join('\n');

// Normalize relative image URLs so bundled CSS works in production.
const merged = mergedRaw.replace(
  /url\((['"]?)(?:\.\.\/|\.\/)?images\//g,
  'url($1/uniquera-consultation-form/assets/images/'
);

const { css } = postcss([
  prefixSelector({
    prefix: PREFIX,
  }),
]).process(merged, { from: `${root}/scoped-bundle.css` });

const outDir = path.join(root, 'src/generated');
fs.mkdirSync(outDir, { recursive: true });
const outPath = path.join(outDir, 'uniquera-form-scoped.css');
fs.writeFileSync(
  outPath,
  `/* Generated by npm run scope-uniquera-css — do not edit */\n${css}\n`,
  'utf8'
);

console.log(`Wrote ${path.relative(root, outPath)}`);
